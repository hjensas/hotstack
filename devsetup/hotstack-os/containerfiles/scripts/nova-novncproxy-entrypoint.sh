set -e

# Wait for Nova API
echo "Waiting for Nova API..."
for _ in {1..60}; do
    if curl -f http://nova:8774/ &>/dev/null; then
        echo "Nova API is ready!"
        break
    fi
    sleep 2
done

# Start Nova NoVNC Proxy
echo "Starting Nova NoVNC Proxy..."
exec nova-novncproxy --config-file=/etc/nova/nova.conf
